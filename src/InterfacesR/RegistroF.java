    public boolean vacio(){        
        boolean band = false;        
        if(tema.getText().isEmpty() || fecha.getDate() == null || hora.getText().isEmpty() || lugar.getText().isEmpty() || 
                duracion.getText().isEmpty() || descripcion.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, " Campos vacios \n no se puede finalizar el registro complete los datos");
            band = true;
        }
        if((JTSelect.getModel().getRowCount()  == 0) && band != true){
            JOptionPane.showMessageDialog(null, "Error seleciona uno o mas Ponentes");
            band = true;
        }
        if(coor.getSelectedIndex() == 0){
            band = true;
            JOptionPane.showMessageDialog(null, "Error seleciona un coordinador");
        }
        return band;
    }    
    
    public void saveF(){
        Personal p = new Personal();
        String f = c.getFecha(fecha);
        String clave = c.generaClaveF(tema.getText(), lugar.getText(), f, coor.getSelectedItem().toString());
        Foro fo = new Foro(clave, tema.getText(),duracion.getText(), lugar.getText(), descripcion.getText(), 1, f, coor.getSelectedItem().toString(), hora.getText());
        p.registraForo(fo);
        p.registraLI(JTSelect,"Foro", clave);
        c.rActv("Foro", clave);
    }
