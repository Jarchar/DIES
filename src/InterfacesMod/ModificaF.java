    public void completa(ArrayList campos){
        if(campos == null){
            JOptionPane.showMessageDialog(null, "Error lista vacia");
        }else{
            Date f = c.StringToDate(campos.get(6).toString());
            tema.setText(campos.get(1).toString());
            duracion.setText(campos.get(2).toString());
            lugar.setText(campos.get(3).toString());
            descripcion.setText(campos.get(4).toString());
            fecha.setDate(f);
            coor.setSelectedItem(campos.get(7).toString());
            hora.setText(campos.get(8).toString());
        }
    }

    public void actualizaF(){
        Personal p = new Personal();
        String f = c.getFecha(fecha);
        Foro fo = new Foro(this.clave, tema.getText(),duracion.getText(), lugar.getText(), descripcion.getText(), 1, f, coor.getSelectedItem().toString(), hora.getText());
        if(cb.isSelected()){
            p.ModificaForo(cb, fo);
        }else{
            System.out.print(fo.getFecha());
            p.ModificaForo(cb, fo);
            p.deleteIP(activiad,clave);
            p.registraLI(JTSelect,"Foro", clave);
        }
    }
    
    public boolean vacio(){
        boolean band = false;
        if(cb.isSelected()){
            return band;
        }else{
            if(tema.getText().isEmpty() || fecha.getDate() == null || hora.getText().isEmpty() || lugar.getText().isEmpty() || 
                    duracion.getText().isEmpty() || descripcion.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, " Campos vacios \n no se puede finalizar el registro complete los datos");
                band = true;
            }
            if((JTSelect.getModel().getRowCount()  == 0) && band != true){
                JOptionPane.showMessageDialog(null, "Error seleciona uno o mas Ponentes");
                band = true;
            }
            if(coor.getSelectedIndex() == 0){
                band = true;
                JOptionPane.showMessageDialog(null, "Error seleciona un coordinador");
            }
        }
        return band;
    }
