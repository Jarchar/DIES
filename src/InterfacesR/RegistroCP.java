    //Funcion para la validacion de los campos.
    public boolean vacio(){
        boolean band = false;        
        if(tema.getText().isEmpty() || costo.getText().isEmpty() || 
                fecha.getDate() == null || 
                hora.getText().isEmpty() || lugar.getText().isEmpty() || 
                duracion.getText().isEmpty() || descripcion.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, " Campos vacios \n no se puede finalizar el registro complete los datos");
            band = true;
        }else if((Integer.parseInt(cupo_max.getValue().toString()) > 0 && Integer.parseInt(cupo_min.getValue().toString()) > 0) && band != true){
            if(Integer.parseInt(cupo_max.getValue().toString()) <= Integer.parseInt(cupo_min.getValue().toString())){
                System.out.println("cp_max:"+cupo_max.getValue().toString());
                System.out.println("cp_min:"+cupo_min.getValue().toString());
                JOptionPane.showMessageDialog(null, "Error el cupo maximo debe ser mayor al cupo minimo");
                band = true;                
            }
        }else{
            JOptionPane.showMessageDialog(null, "Error el cupo maximo y el cupo minimo deben ser mayores a 0");
            band = true;
        }
        if((JTSelect.getModel().getRowCount()  == 0) && band != true){
            JOptionPane.showMessageDialog(null, "Error seleciona uno o mas instructores");
            band = true;
        }        
        return band;
    }
    
    //funcion que registra cualquiera de las 2 actividades ( Platica o conferencia)
    public void registraPC(String tipo){
        Personal p = new Personal();
        String fechaIni = c.getFecha(fecha);        
        Double d = parseDouble(costo.getText());
        String clave = c.generaClave(tema.getText(), lugar.getText(), costo.getText(), cupo_max.getValue().toString(), fechaIni);
        System.out.println(clave);
        if (tipo.equals("Platica")){
            Platica pl = new Platica(clave,fechaIni,tema.getText(),duracion.getText(),d, lugar.getText(), Integer.parseInt(cupo_max.getValue().toString()), 
            Integer.parseInt(cupo_min.getValue().toString()), descripcion.getText(),true, hora.getText());
            p.registraPlatica(pl);
            p.registraLI(JTSelect, tipo, clave);
            c.rActv(this.tipo, clave);
        }else if (tipo.equals("Conferencia")){
            Conferencia cf = new Conferencia(clave,fechaIni,tema.getText(),duracion.getText(),d, lugar.getText(), Integer.parseInt(cupo_max.getValue().toString()), 
            Integer.parseInt(cupo_min.getValue().toString()), descripcion.getText(),true, hora.getText());
            p.registraConfe(cf);
            p.registraLI(JTSelect, tipo, clave);
            c.rActv(this.tipo, clave);
        }        
    }
    
