    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(vacio() == false){
            int opcion = JOptionPane.showConfirmDialog(null, "¬øEstas seguro de finalizar el registro?", "Registro", JOptionPane.YES_NO_CANCEL_OPTION);
            if(opcion == JOptionPane.YES_OPTION){                    
                saveF();
                JOptionPane.showMessageDialog(null, "Actividad registrada correctamente.");
                Principal prin = new Principal();
                prin.show();
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Registro de actividad cancelado.");
            }
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

